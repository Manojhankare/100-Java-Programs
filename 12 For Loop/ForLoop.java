package aa12_For_Loop;

public class ForLoop {
	// Simple For Loop demo

	public static void main(String[] args) {
		int i;

		for (i = 101; i <= 10; i++) {
			System.out.println(i);// printing no ..
		}
		for (i = 101; i >= 10; i--) {
			System.out.println(i);// printing no in reverse..
		}
	}
}
