public class IfElse {

	public static void main(String[] args) {
		//If condition is True it will run methods from if loop
		int i = 55;
		
		if ( i > 50) {
			System.out.println("Condition True, No is greater than 50");
		}
		else {
			System.out.println("No is less than 50");
		}
	}

}
